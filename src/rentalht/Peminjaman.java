    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String nm = String.valueOf(nama.getText());
        String Noktp = String.valueOf(noktp.getText());
        String Alamat = String.valueOf(alamat.getText());
        String Nohp = String.valueOf(nohp.getText());
        String Lamasewa = String.valueOf(lamasewa.getText());
        String Banyakht = String.valueOf(banyakht.getText());
        String Dp = String.valueOf(dp.getText());
        try {
            int row = Sis.getSelectedRow();
            get_id = tab.getValueAt(row, 1);
            sql = "UPDATE data_peminjaman SET nama='" + nm + "',noktp='" + Noktp + "',alamat='"
                    + Alamat + "' ,nohp='" + Nohp + "',lamasewa='" + Lamasewa + "',banyakht='" + Banyakht + ""
                    + "',dp='" + Dp + "' WHERE id_peminjam = '"+get_id+"'";
            conn.setStatement(conn.getConnect().createStatement());
            conn.getStatement().execute(sql);
            getData();
            JOptionPane.showMessageDialog(null, "Update Data Sukses");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update Data Gagal" + e.getMessage());
        } finally {
            try {
                conn.getResultSet().close();
                conn.getPreStatement().close();
            } catch (Exception e) {
            }
        }

    }//GEN-LAST:event_updateActionPerformed

