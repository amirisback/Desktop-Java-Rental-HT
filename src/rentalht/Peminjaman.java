    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        nm=nama.getText();
        no=noktp.getText();
        almt=alamat.getText();
        nohpp=nohp.getText();
        lm=lamasewa.getText();
        ml=(String)sdf.format(cal.getTime());
        tgl=(String)sdt.format(cal.getTime());
        bykht=banyakht.getText();
        dpp=dp.getText();
        try {   
            sql = "INSERT INTO data_peminjaman(id_peminjam, nama, noktp, "
                    + "alamat, nohp, lamasewa, mulaisewa, tanggal, banyakht, dp) "
                    + "VALUES ('" + func.getRandomChar() + "','" + nm + "','" + no + "',"
                    + "'" + almt + "','" + nohpp + "','" + lm + "','" + ml + "','" + tgl + "','" + bykht + "',"
                    + "'" + dpp + "')";
            conn.setStatement(conn.getConnect().createStatement());
            conn.getStatement().execute(sql);
            getData();
            JOptionPane.showMessageDialog(null,"berhasil disimpan");
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Maaf data anda tidak disimpan\n"
                + "error pada "+ e.getMessage());
        } finally { try{ conn.getResultSet().close(); conn.getPreStatement().close(); } catch(Exception e) { } }
    }//GEN-LAST:event_simpanActionPerformed
    
